	private final long value;

	/**
	 * Constructor
	 */
	private PRIVILEGES() {
		this.value = calculateValue();
	}

	/**
	 * Returns the value of the given privilege
	 *
	 * @return value
	 */
	public long getValue() {
		return value;
	}

	/**
	 * Calculates the value of the given privilege
	 *
	 * The value is the binary number that is calculated from the
	 * position of the privilege in the list.
	 *
	 * So "ADMIN_HAS_ROLE, // 49" results to:
	 * 0b00000000_00000001_00000000_00000000_00000000_00000000_00000000_00000000L or 281474976710656
	 *
	 * (means: the 49th digit of the long value is a 1, all the others are 0)
	 *
	 * @return value
	 */
	public long calculateValue() {
		long value = 1;
		for (int i = 0; i < this.ordinal(); i++) {
			value = value << 1;
		}
		return value;
	}
